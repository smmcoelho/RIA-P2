# Tipos de Variáveis 

# int
num = 5 # numero inteiro
print(f"O numero {num} 'e do tipo {type(num)}")

# float
numd = 5.5 # numero real 
print(f"O numero {numd} 'e do tipo {type(numd)}")

# str (string)
text = "hello" # texto
print(f"O numero {text} 'e do tipo {type(text)}")

# bool (boolean)
isValid = True # booleano (True or False)
print(f"O numero {isValid} 'e do tipo {type(isValid)}")