# Estruturas de dados


# list
l = [1, 3, 3, 5, 9, 7]
print(f"A variable do tipo {type(l)} tem os valores: {l} ")

# tuple
t = (1, 3, 3, 5, 9, 7) # imotavel
print(f"A variable do tipo {type(t)} tem os valores: {t} ")

# set
s = {1, 3, 3, 5, 9, 7} # ordenado e nao permite duplicados
print(f"A variable do tipo {type(s)} tem os valores: {s} ")

# dic (dictionary)
d = {1: "Name", 2: "Surname"}
print(f"A variable do tipo {type(d)} tem os valores: {d} ")
