# Operadores Aritmeticos

# int
num1 = 5 # numero inteiro 
num2 = 5
print(f"O numero {num1 + num2} ")

# float
numd1 = 5.5 # numero real 
numd2 = 5.5
print(f"O numero {numd1 + numd2}")

# str (string)
text1 = "hello" # texto
text2 = "hello"
print(f"O numero {text1 + text2}")

# bool (boolean)
isValid1 = True # booleano (True or False)
isValid2 = False
print(f"O numero {isValid1 + isValid2}")


# tipos diferentes devem ser convertidos
text3 = 'hello'
num3 = 5
print(f"O numero {text3 + str(num3)} ")
